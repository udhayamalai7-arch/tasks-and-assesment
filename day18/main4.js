let marks = 5;

if (marks >= 35 && marks <= 40) {
  console.log("Grade: F");
} 
else if (marks >= 75 && marks < 80) {
  console.log("Grade: C");
} 
else if (marks >= 80 && marks < 90) {
  console.log("Grade: D");
} 
else if (marks >= 90) {
  console.log("Grade: A");
} 
else {
  console.log("You are fail");
}
